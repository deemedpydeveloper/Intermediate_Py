# Instances of an Object.

from turtle import Turtle

turtle1 = Turtle()  # constructing an object from class.

turtle2 = Turtle()  # constructing another object from class.

## Both may or may not be differing in attributes and methods they hold.
## They are completely independent in operational-wise as if they are differnt.
## These type of multiple objects are known as "Instances of Objects".
## The attribute or method which is different when compared to each other is known as a 'State'.