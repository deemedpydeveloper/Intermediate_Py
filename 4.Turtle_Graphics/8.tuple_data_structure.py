# Tuple Data Structure.

## Syntax ----> (list_of_items separated by comma).

my_tuple = (1, 2, 3)
print(my_tuple)
print(my_tuple[0])

## Unlike in list, you can't change the ite in tuple. This is called as 'Immutable'.
print(list(my_tuple))   # DataStructure Conversion for changing the values.