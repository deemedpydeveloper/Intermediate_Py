# Slicing Method.

piano_keys = ['a', 'b', 'c', 'd', 'e', 'f', 'g']

# slices from 2nd position to 5th position.
print(piano_keys[2:5])

# slices from 2nd position to end of the list.
print(piano_keys[2:])

# slices from start to 5th position.
print(piano_keys[:5])

# slices from 2nd position with a step of 2.
print(piano_keys[2:5:2])

# slices from end of list to start of the list.
print(piano_keys[::-1])

## the above method is also applicable to tuple.